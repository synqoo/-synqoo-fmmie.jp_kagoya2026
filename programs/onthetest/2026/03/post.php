興" rel="noopener" style="cursor: pointer;" href="https://search.yahoo.co.jp/search?ei=UTF-8&amp;rkf=1&amp;slfr=1&amp;qrw=0&amp;p=%E6%96%B0%E8%88%88&amp;fr=link_kw_nws_direct&amp;fr2=cid_56231cd0c9fd0f5d55e2c1b0e926ce0c173cf0df" target="_blank" data-cl_cl_index="325">新興</a>技術センター（CSET）のシニアリサーチアナリストを務めるローレン・カーン氏は、アンソロピックのAIシステムが「優れた能力」を持つとした上で、これを外すのは「関係者全員にとって痛みを伴う」と語った。 高官は5日、「当初から根本的な原則は一つだった。軍があらゆる合法的な目的のために技術を使用できることだ」と指摘。「重要な能力の合法的な使用を制限して指揮系統に介入し、わが国の戦闘員を危険にさらすことを、軍はベンダーに許さない」と強調した。</p>
              
