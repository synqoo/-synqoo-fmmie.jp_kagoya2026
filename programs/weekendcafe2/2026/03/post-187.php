              <p>おはようございます。ウィークエンドカフェへようこそ。<br />海では春の魚が動き出し、<br />木々の枝先には、小さな命の気配。<br />見えないところで準備していたものが、<br />静かに、確実に、顔を出しはじめる。<br />ウィークエンドカフェのオープンです。</p>
<p>海山郷土資料館<br />いまから111年前、地元の豪商松永家によって建てられました。モダンな洋風建築で明治末期のヒノキ材がふんだんに使われています。お話しは、資料館の主事、家崎彰さん。一歩足を踏み入れると、きしむ床の音は、物語のはじまりのようです。</p>
<p><img alt="PXL_20260303_020738935.jpg" src="https://fmmie.jp/program/weekendcafe2/phoros/PXL_20260303_020738935.jpg" width="800" height="602" class="mt-image-none" /><img alt="PXL_20260303_021310529.MP.jpg" src="https://fmmie.jp/program/weekendcafe2/phoros/PXL_20260303_021310529.MP.jpg" width="800" height="602" class="mt-image-none" /></p>
<p>現在開催中の企画展は「奥熊野の戦国時代」。紀伊半島をめぐる勢力争いのなかで、この地域もまた、戦国の舞台となっていたのです。</p>
<p><img alt="PXL_20260303_020819384.jpg" src="https://fmmie.jp/program/weekendcafe2/phoros/PXL_20260303_020819384.jpg" width="800" height="602" class="mt-image-none" /></p>
<p>戦国の鼓動は、尾鷲や紀北にも響いていました。<br />山の尾根に築かれた山城、海を見下ろす砦、堀内氏の勝利によって、ここは志摩の国から、紀伊の国へと変わりました。</p>
<p><img alt="PXL_20260303_020807823.jpg" src="https://fmmie.jp/program/weekendcafe2/phoros/PXL_20260303_020807823.jpg" width="800" height="602" class="mt-image-none" /></p>
<p>昔の出来事を知ることは、いま自分たちが立っている場所を知ること。<br />企画展を通じて、何を受け渡していくのかを考えています。<br />穏やかな風景の奥に眠る、戦国の物語。<br />歴史は、いまもこの土地に息づいています。</p>
